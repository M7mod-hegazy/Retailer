function normalizeStructuredReport(data) {
  if (!data || typeof data !== "object" || Array.isArray(data)) return null;
  if (Array.isArray(data.rows)) {
    return {
      rows: data.rows,
      sections: Array.isArray(data.sections) ? data.sections : [],
      summary: data.summary || null,
    };
  }
  if (Array.isArray(data.transactions)) {
    const summary = { ...data };
    delete summary.transactions;
    return { rows: data.transactions, sections: [], summary };
  }
  const sections = [];
  ["by_payment", "by_range"].forEach((key) => {
    if (Array.isArray(data[key])) {
      const sectionLabel = key === "by_payment" ? "حسب طريقة الدفع" : "حسب نطاق الخصم";
      sections.push({ key, label: sectionLabel, rows: data[key] });
    }
  });
  if (sections.length) {
    const rows = sections.flatMap((section) => section.rows.map((row) => ({ section: section.label, ...row })));
    return { rows, sections, summary: null };
  }
  return null;
}


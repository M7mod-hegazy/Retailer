function orderKeys(keys, slug) {
  const preferred = REPORT_COLUMN_KEYS[slug] || [];
  const used = new Set();
  const ordered = [];
  preferred.forEach((key) => {
    if (keys.includes(key) && !used.has(key)) {
      ordered.push(key);
      used.add(key);
    }
  });
  keys.forEach((key) => {
    if (!used.has(key)) ordered.push(key);
  });
  return ordered;
}

function getReportColumns(slug, rows = []) {
  const rowKeys = Object.keys(rows?.[0] || {});
  const catalogKeys = REPORT_COLUMN_KEYS[slug] || [];
  const keys = rowKeys.length ? orderKeys(rowKeys, slug).filter(k => !k.startsWith('_')) : catalogKeys;
  const extras = REPORT_EXTRA_KEYS[slug];
  let coreIndex = 0;
  return keys.map((key) => {
    const hidden = INTERNAL_KEYS.has(key) || (extras ? extras.has(key) : false);
    return buildColumn(key, hidden ? Infinity : coreIndex++, hidden);
  });
}

